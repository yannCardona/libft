#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src,
size_t size)
{
	size_t	l;
	size_t	i;

	l = ft_strlen(src);
	if (size == 0)
		return (l);
	i = 0;
	while (i < size - 1 && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (l);
}
